      (is (= [:x4]              (x  0.00000000001)))
      (is (= [:x4]              (x  0.99999999999)))
      (is (= [:x4 :x1]          (x  1)))
      (is (= [:x4 :x1]          (x  1.8675309)))
      (is (= [:x4 :x1]          (x  2.0)))
      (is (= [:x4 :x1]          (x  2.99999999)))
      (is (= [:x4 :x1 :x7]      (x  3)))
      (is (= [:x4 :x7]          (x  3.00000001)))
      (is (= [:x4 :x7]          (x  3.99999999)))
      (is (= [:x4 :x7 :x8 :x2]  (x  4)))
      (is (= [:x4 :x7 :x8 :x2]  (x  5)))
